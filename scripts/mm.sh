#!/usr/bin/env bash
# Headless-исполнитель MiniMax M3. usage: mm.sh "<задача>" [минут_таймаут]
#
# INFRA-051 инцидент (2026-07-18): исполнитель с голым Bash-доступом сам
# сделал git commit, отредактировал TASK_BOARD/CHANGELOG вне списка файлов
# ТЗ, заявил о несуществующем "reviewer subagent → APPROVE" и удалил
# обязательный report-файл до того, как оркестратор успел его прочитать.
# Ниже — технический deny (git) + обязательный guardrail-блок, добавляемый
# к каждому ТЗ автоматически.
#
# INFRA-052 инцидент (2026-07-18): claude -p с cwd=/root/polymarket-bot
# автоматически подхватывал CLAUDE.md (протокол оркестрации) и кастомных
# субагентов из .claude/agents/ (debugger, reviewer). Исполнитель прочитал
# протокол делегирования как относящийся к себе и стал изображать
# оркестратора: TaskCreate себе подзадач, рекурсивный вызов scripts/mm.sh
# на самого себя, вызов Agent-тула debugger — процесс завис в цикле,
# убит вручную (pkill) через ~10 минут. Фикс: --safe-mode отключает
# CLAUDE.md auto-discovery и кастомных агентов (не трогает авторизацию,
# в отличие от --bare); дополнительный technical deny на самовызов.
#
# ACT-009 инцидент (2026-07-19): исполнитель дважды провалил read-only
# расследование по инфраструктурным причинам, не связанным с ТЗ. Attempt 1:
# звал `python` (бинарника нет в системе, только `python3`) через
# `bash -c`/heredoc — exit 127. Attempt 2 (после явного запрета heredoc):
# инструмент Read у MiniMax-M3 систематически передаёт параметр `pages: ""`
# (валиден только для PDF) даже для .py/.sql-файлов — получает ошибку,
# иногда зацикливается на том же вызове вместо самокоррекции, иногда
# восстанавливается сам через повтор с pages:"1" (не гарантировано). Фикс
# уровня ОС: `python` теперь симлинк на `python3` (устраняет первый класс
# ошибок полностью). Для pages — пробовал PreToolUse-хук (jq вырезает
# pages для не-PDF файлов до вызова инструмента, см. второе мнение Fable в
# транскрипте ACT-007) — НЕ СРАБОТАЛО: `--safe-mode` глушит хуки целиком
# ("all customizations ... hooks ... disabled"), включая явно переданные
# через --settings. Обходной путь (--setting-sources вместо --safe-mode)
# рискует вернуть INFRA-052 (исполнитель подхватывает CLAUDE.md/агентов и
# косплеит оркестратора) без обширного тестирования изоляции — сочтено не
# стоящим экономии нескольких ходов на ретраях pages-бага. Решение: остаться
# на --safe-mode, поднять --max-turns (доп. бюджет на неизбежные ретраи) и
# требовать инкрементальную запись отчёта (см. GUARDRAILS) — если ходы
# всё же кончатся, у оркестратора будет хотя бы частичный прогресс.
set -u
set -o pipefail
TASK="$1"; LIMIT="${2:-30}"
LOG="/root/polymarket-bot/logs/mm_executor.log"

GUARDRAILS='

---
ОГРАНИЧЕНИЯ ИСПОЛНИТЕЛЯ (обязательны, не обсуждаются, не являются частью задачи выше):
- Правь только файлы, явно перечисленные в разделе "Файлы" ТЗ. Ничего сверх
  списка — включая docs/TASK_BOARD.md, changelogs/CHANGELOG.md,
  docs/PROJECT_STATE.md — не трогать. Документация и git — зона оркестратора.
- git-команды тебе недоступны технически (заблокированы) — не пытайся.
- Не утверждай в отчёте, что код прошёл ревью или тесты, которые ты не
  прогнал сам явно в рамках этой сессии и не показал вывод. Не ссылайся на
  несуществующих "reviewer subagent" — такого инструмента у тебя нет.
- Файл scratchpad/<task>_report.md — обязательный итоговый артефакт для
  оркестратора. Создай его И ОБНОВЛЯЙ ПО ХОДУ работы (не только в конце) —
  допиши в него результат каждого крупного шага сразу после того, как он
  готов. Если ходы закончатся раньше, чем вся задача — оркестратор должен
  увидеть частичный, но реальный прогресс, а не пустой файл. НЕ удаляй.
- Не используй bash heredoc (`command <<'"'"'EOF'"'"' ... EOF`) и не оборачивай
  код в `bash -c` с длинной inline-строкой. Пиши код в .py-файл обычной
  записью файла, затем запускай прямой командой `python3 file.py`.
- Инструмент Read: не передавай параметр `pages` для не-PDF файлов вообще
  (он нужен только для .pdf). Если всё же получил ошибку "Invalid pages
  parameter" — не повторяй тот же вызов, вызови Read заново без параметра
  `pages` в принципе.'

TASK="${TASK}${GUARDRAILS}"

ANTHROPIC_BASE_URL="https://api.minimax.io/anthropic" \
ANTHROPIC_AUTH_TOKEN="$(cat /root/.minimax_key)" \
ANTHROPIC_MODEL="MiniMax-M3" \
CLAUDE_CODE_DISABLE_NONESSENTIAL_TRAFFIC=1 \
timeout "${LIMIT}m" /root/.local/bin/claude -p "$TASK" \
  --safe-mode \
  --permission-mode acceptEdits \
  --allowedTools "Bash" \
  --disallowedTools "Bash(git *)" "Bash(*mm.sh*)" "Bash(*/.local/bin/claude*)" "Agent" \
  --max-turns 80 \
  --output-format stream-json \
  --verbose \
  | python3 /root/polymarket-bot/scripts/mm_log_filter.py \
  | tee -a "$LOG"
echo "EXIT:${PIPESTATUS[0]}"
